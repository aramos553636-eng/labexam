package alfertramos;

class Item {
    String code;
    String name;
    int quantity;

    Item(String code, String name, int quantity) {
        this.code = code;
        this.name = name;
        this.quantity = quantity;
    }

    public String toString() {
        return code + " | " + name + " | " + quantity;
    }
}
	
		
	
	
	
	
	
	
	
	
	
	

